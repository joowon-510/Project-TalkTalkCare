import React, {
  createContext,
  useContext,
  useState,
  useEffect,
  useRef,
  useCallback
} from 'react';
import { useNavigate } from 'react-router-dom';
import { Friend } from '../components/main_page/friends';
import CustomModal from '../components/CustomModal';
import CallNotificationModal from '../components/CallNotificationModal';
import openviduService from '../services/openviduService';

const WS_URL = import.meta.env.VITE_API_WS_URL;
const BASE_URL = import.meta.env.VITE_API_BASE_URL;

export interface GameEvent {
  eventType: 'GAME_SELECTED' | 'GAME_DESELECTED' | 'SKILL_CHANGED';
  gameId?: string;
  skill?: string;
  senderId?: string;
}

export interface CallInvitationDto {
  callerId: number;
  callerName: string;
  receiverId: number;
  receiverName: string;
  message: string;
  openviduSessionId: string;
}

interface WebSocketContextType {
  isConnected: boolean;
  setIsLoggedIn: (value: boolean) => void;
  onFriendStatusUpdate: (callback: (friends: Friend[]) => void) => void;
  acceptCall: () => Promise<void>;
  rejectCall: () => Promise<void>;
  sendGameEvent: (data: GameEvent) => void;
  onGameSelected: (callback: (event: GameEvent) => void) => void;
  wsModalOpen: boolean;
}

const WebSocketContext = createContext<WebSocketContextType | null>(null);

export const WebSocketProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const navigate = useNavigate();
  const [isLoggedIn, setIsLoggedIn] = useState<boolean>(false);
  const [ws, setWs] = useState<WebSocket | null>(null);
  const [isConnected, setIsConnected] = useState<boolean>(false);
  const [isModalOpen, setIsModalOpen] = useState<boolean>(false);
  const [modalMessage, setModalMessage] = useState<string>("");
  const [modalSource, setModalSource] = useState<"ws" | "local" | null>(null);
  const friendStatusCallbackRef = useRef<((friends: Friend[]) => void) | undefined>();
  const gameSelectionCallback = useRef<(event: GameEvent) => void>();
  const [callInvitation, setCallInvitation] = useState<CallInvitationDto | null>(null);
  const reconnectAttempts = useRef(0);
  const maxReconnectAttempts = 3;

  useEffect(() => {
    const userId = localStorage.getItem('userId');
    setIsLoggedIn(!!userId);
  }, []);

  

  useEffect(() => {
    if (!isLoggedIn) return;
    const userId = localStorage.getItem('userId');
    if (!userId) return;

    const connectWebSocket = () => {
      if (reconnectAttempts.current >= maxReconnectAttempts) {
        //console.log('ÏµúÎåÄ Ïû¨Ïó∞Í≤∞ ÏãúÎèÑ ÌöüÏàò ÎèÑÎã¨');
        return;
      }
      try {
        const websocket = new WebSocket(`${WS_URL}?userId=${userId}`);
        websocket.onopen = () => {
          //console.log('‚úÖ WebSocket Ïó∞Í≤∞Îê®');
          setIsConnected(true);
          reconnectAttempts.current = 0;
        };
        websocket.onmessage = async (event) => {
          try {
            const data = JSON.parse(event.data);
            //console.log("WebSocket Î©îÏãúÏßÄ ÏàòÏã†:", data);

            // ÌôîÏÉÅÌÜµÌôî ÏöîÏ≤≠ Ï≤òÎ¶¨
            if (data.message && data.message.includes("ÌôîÏÉÅÌÜµÌôî")) {
              setCallInvitation(data);
            }
            // receiverÍ∞Ä ÌôîÏÉÅÌÜµÌôîÎ•º ÏàòÎùΩÌñàÎã§Îäî Í≤ÉÏùÑ callerÏóêÍ≤å ÏïåÎ¶º
            if (data.message && data.message.includes("ÏàòÎùΩÌïòÏòÄÏäµÎãàÎã§")) {
              const acceptedData = data as CallInvitationDto;
              localStorage.setItem('opponentUserId', acceptedData.receiverId.toString());
              await openviduService.joinSession(acceptedData.openviduSessionId); //caller Ï†ëÏÜç
              localStorage.setItem('currentSessionId', acceptedData.openviduSessionId);
              navigate('/videocall');
            }
            // ÌôîÏÉÅÌÜµÌôî Í±∞Ï†à Ïãú Ï≤òÎ¶¨
            if (data.message && data.message.includes("Í±∞Ï†àÌïòÏòÄÏäµÎãàÎã§")) {
              const acceptedData = data as CallInvitationDto;
              localStorage.removeItem('currentSessionId');
              localStorage.removeItem('opponentUserId');
              setModalMessage(`${acceptedData.receiverName}ÎãòÍªòÏÑú ÌôîÏÉÅÌÜµÌôî ÏöîÏ≤≠ÏùÑ Í±∞Ï†àÌïòÏÖ®ÏäµÎãàÎã§.`);
              setModalSource("ws");  // WS Ï∂úÏ≤ò ÏßÄÏ†ï
              setIsModalOpen(true);
              
            }

            // ÏπúÍµ¨ ÏÉÅÌÉú ÏóÖÎç∞Ïù¥Ìä∏ (ÏòµÏÖò)
            if (friendStatusCallbackRef.current && Array.isArray(data)) {
              friendStatusCallbackRef.current(data);
            }
            // Í≤åÏûÑ Ïù¥Î≤§Ìä∏ ÏàòÏã† Ï≤òÎ¶¨
            if (data.eventType) {
              //console.log("Í≤åÏûÑ Ïù¥Î≤§Ìä∏ ÏàòÏã†:", data);
              if (gameSelectionCallback.current) {
                gameSelectionCallback.current(data);
              }
            }
          } catch (error) {
            //console.error('WebSocket Î©îÏãúÏßÄ Ï≤òÎ¶¨ Ïò§Î•ò:', error);
          }
        };
        websocket.onclose = (event) => {
          //console.log('‚ùå WebSocket Ïó∞Í≤∞ Ï¢ÖÎ£å');
          setIsConnected(false);
          setWs(null);
          if (event.code !== 1000) {
            reconnectAttempts.current += 1;
            if (reconnectAttempts.current < maxReconnectAttempts) {
              //console.log(`Ïû¨Ïó∞Í≤∞ ÏãúÎèÑ ${reconnectAttempts.current}/${maxReconnectAttempts}`);
              setTimeout(connectWebSocket, 3000);
            }
          }
        };
        setWs(websocket);
      } catch (error) {
        //console.error('ÏõπÏÜåÏºì Ïó∞Í≤∞ Ï§ë Ïò§Î•ò:', error);
        reconnectAttempts.current += 1;
      }
    };

    connectWebSocket();

    return () => {
      if (ws) {
        ws.close(1000, "Ï†ïÏÉÅ Ï¢ÖÎ£å");
      }
    };
  }, [isLoggedIn, navigate]);

  const handleAcceptCall = async () => {
    if (callInvitation) {
      //console.log('ÌôîÏÉÅÌÜµÌôî ÏàòÎùΩ:', callInvitation);
      try {
        // receiver
        await openviduService.joinSession(callInvitation.openviduSessionId);
        localStorage.setItem('currentSessionId', callInvitation.openviduSessionId);
        localStorage.setItem('opponentUserId',callInvitation.callerId.toString());

        // Î∞±ÏóîÎìúÎ°ú /call/accept ÏöîÏ≤≠ Ï†ÑÏÜ°ÌïòÏó¨ callerÏóêÍ≤å ÏàòÎùΩ Î©îÏãúÏßÄ Ï†ÑÏÜ°
        await fetch(`${BASE_URL}/call/accept`, {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
          },
          body: JSON.stringify({
            receiverId: callInvitation.receiverId,
            callerId: callInvitation.callerId,
            openviduSessionId: callInvitation.openviduSessionId,
          }),
          credentials: 'include',
        });

        navigate('/videocall');

      } catch (error) {
        //console.error('Receiver ÏÑ∏ÏÖò Ï†ëÏÜç Ïã§Ìå®:', error);
      }
      setCallInvitation(null);
    }
  };

   // Í±∞Ï†à Î≤ÑÌäº ÌÅ¥Î¶≠ Ïãú, /call/reject ÏöîÏ≤≠ÏùÑ Î≥¥ÎÇ¥ callerÏóêÍ≤å ÏïåÎ¶º
   const handleRejectCall = async () => {
    if (callInvitation) {
      //console.log('ÌôîÏÉÅÌÜµÌôî Í±∞Ï†à:', callInvitation);
      try {
        await fetch(`${BASE_URL}/call/reject`, {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
          },
          body: JSON.stringify({
            receiverName: callInvitation.receiverName,
            callerId: callInvitation.callerId,
            openviduSessionId: callInvitation.openviduSessionId,
          }),
          credentials: 'include',
        });
      } catch (error) {
        //console.error('call/reject ÏöîÏ≤≠ Ï§ë ÏóêÎü¨:', error);
      }
      setCallInvitation(null);
    }
  };


  // ÏõπÏÜåÏºìÏúºÎ°ú Í≤åÏûÑ Ïù¥Î≤§Ìä∏ Ï†ÑÏÜ° (Î∞îÎ°ú ws.send ÏÇ¨Ïö©)
  const sendGameEvent = (data: GameEvent) => {
    if (ws && isConnected) {
      const userId = localStorage.getItem('userId');
      const enrichedData = { ...data, senderId: userId };
      //console.log('üì§ WebSocket Ïù¥Î≤§Ìä∏ Ï†ÑÏÜ°:', enrichedData);
      ws.send(JSON.stringify(enrichedData));
    } else {
      //console.log('‚ö†Ô∏è WebSocket Ïó∞Í≤∞ ÏïàÎê®: Ïù¥Î≤§Ìä∏ Ï†ÑÏÜ° Ïã§Ìå®');
    }
  };

  // onGameSelected ÏΩúÎ∞± Îì±Î°ù
  const onGameSelected = useCallback((callback: (event: GameEvent) => void) => {
    //console.log('üü¢ onGameSelected() Ïã§ÌñâÎê®, ÏΩúÎ∞± Îì±Î°ù:', callback);
    gameSelectionCallback.current = callback;
  }, []);

  const contextValue: WebSocketContextType = {
    isConnected,
    setIsLoggedIn,
    onFriendStatusUpdate: useCallback((callback?: (friends: Friend[]) => void) => {
      friendStatusCallbackRef.current = callback;
    }, []),
    acceptCall: handleAcceptCall,
    rejectCall: handleRejectCall,
    sendGameEvent,
    onGameSelected,
    wsModalOpen: isModalOpen,
    modalSource,
  };

  return (
    <WebSocketContext.Provider value={contextValue}>
      {children}
      {callInvitation && (
        <CallNotificationModal
          title="ÌôîÏÉÅÌÜµÌôî ÏöîÏ≤≠"
          message={`${callInvitation.callerName}ÎãòÍªòÏÑú ${callInvitation.receiverName}ÏóêÍ≤å ÌôîÏÉÅÌÜµÌôî ÏöîÏ≤≠ÏùÑ Î≥¥ÎÉàÏäµÎãàÎã§. ÏàòÎùΩÌïòÏãúÍ≤†ÏäµÎãàÍπå?`}
          isOpen={true}
          onAccept={handleAcceptCall}
          onReject={handleRejectCall}
        />
      )}
  
      {/* Î™®Îã¨ ÏÉÅÌÉúÏóê Îî∞Î•∏ CustomModal Î†åÎçîÎßÅ */}
      {isModalOpen && (
        <CustomModal
          title="ÏïåÎ¶º"
          message={modalMessage}
          isOpen={isModalOpen}
          onClose={() => {
            setIsModalOpen(false);
            setModalSource(null);
          }}
          
        />
      )}
    </WebSocketContext.Provider>
  );
  
};

export const useWebSocket = () => {
  const context = useContext(WebSocketContext);
  if (!context) {
    throw new Error('useWebSocket must be used within a WebSocketProvider');
  }
  return context;
};
